def execute_tasks(ai_tasks_list, assumptions):
    """
    Executes the list of tasks generated by the supervisor agent.
    Args:
        ai_tasks_list (list): List of tasks generated by the supervisor agent.
        assumptions (list): List of assumptions from input analyzer.
    Returns:
        tuple: (task_outputs, total_input_tokens_count, total_output_tokens_count, generated_snowflake_queries_list, requires_clarification)
    """
    task_outputs = []  # List to store task outputs with sequence IDs
    total_input_tokens_count = 0
    total_output_tokens_count = 0
    generated_snowflake_queries_list = []
    requires_clarification = False
 
    for i, task in enumerate(ai_tasks_list, start=1):
        function_name = task.get("function_name")
        function_params = task.get("function_params")
        task_id = i
 
        print(f"Executing Task ID: {task_id}, Function Name: {function_name}, Params: {function_params}")
        if function_name == "human_agent":
            requires_clarification = True
            print("Human agent detected - clarification required")
 
        # Inject interpretation into Snowflake input_text if applicable
        if function_name == "snowflake_agent" and isinstance(function_params, (dict, str)):
            # If function_params is a list or dict, convert accordingly
            if isinstance(function_params, dict):
                orig = function_params.get("input_text", "")
            else:
                orig = str(function_params)
                
            if assumptions and isinstance(orig, str) and "(Interpretation:" not in orig:
                enhanced_text = orig + " (Interpretation: " + "; ".join(assumptions) + ")"
                if isinstance(function_params, dict):
                    function_params["input_text"] = enhanced_text
                else:
                    function_params = enhanced_text
 
        func = globals().get(function_name)
        if not func:
            print(f"Function '{function_name}' not found.")
            continue
 
        try:
            # Handle parameters (some agents expect string, some expect dict)
            function_output = func(function_params)
            print(f"Function Output for {function_name}:", function_output)
 
            if function_output:
                # If it's the snowflake agent, it returns 5 values
                if function_name == "snowflake_agent" and isinstance(function_output, tuple) and len(function_output) == 5:
                    gen_query, res_json, follow_ups, in_tokens, out_tokens = function_output
                    generated_snowflake_queries_list.append(gen_query)
                    total_input_tokens_count += in_tokens
                    total_output_tokens_count += out_tokens
                    
                    task_outputs.append({
                        "task_id": task_id,
                        "function_name": function_name,
                        "output": res_json  # Main data result
                    })
                else:
                    # Generic handling for other agents (human, summary, etc.)
                    # If it returns (output, in, out) tuple
                    if isinstance(function_output, tuple) and len(function_output) == 3:
                        actual_output, in_tokens, out_tokens = function_output
                        total_input_tokens_count += in_tokens
                        total_output_tokens_count += out_tokens
                        task_outputs.append({
                            "task_id": task_id,
                            "function_name": function_name,
                            "output": actual_output
                        })
                    else:
                        task_outputs.append({
                            "task_id": task_id,
                            "function_name": function_name,
                            "output": function_output
                        })
            else:
                print(f"No output generated for task {task_id} ({function_name}).")
 
        except Exception as e:
            print(f"Error in {function_name}: {str(e)}")
 
    return task_outputs, total_input_tokens_count, total_output_tokens_count, generated_snowflake_queries_list, requires_clarification
 
